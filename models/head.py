class Flatten(nn.Module):

    def __init__(self, c1, c2):  # ch_in, ch_out, kernel, stride, padding, groups
        super().__init__()
        self.pool = nn.AdaptiveAvgPool2d(1)  # to x(b,c_,1,1)
        self.linear = nn.Linear(c1, c2)  # to x(b,c2)
        if c2==1:
            self.use_sigmoid = True
